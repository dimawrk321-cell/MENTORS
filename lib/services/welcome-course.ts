export async function ensureWelcomeCourse(db: Db, dryRun = false): Promise<string> {
  const existing = await db.course.findUnique({
    where: { slug: COURSE_SLUG },
    include: { modules: { include: { lessons: true } } },
  });

  if (existing) {
    const lessonCount = existing.modules.reduce((n, m) => n + m.lessons.length, 0);
    console.log(
      `Курс «${COURSE_TITLE}» уже существует (${existing.status}, ${lessonCount} уроков) — создание пропущено.`,
    );
    return existing.id;
  }

  console.log(
    `${dryRun ? "[dry-run] " : ""}Создаю курс «${COURSE_TITLE}» (slug ${COURSE_SLUG}, gating free, статус draft), ` +
      `1 модуль, ${LESSONS.length} уроков (published).`,
  );
  if (dryRun) return "welcome-dry-run-id";

  const now = new Date();
  const course = await db.course.create({
    data: {
      slug: COURSE_SLUG,
      title: COURSE_TITLE,
      description: COURSE_DESCRIPTION,
      order: 0,
      gating: "free",
      status: "draft",
      modules: {
        create: {
          title: MODULE_TITLE,
          order: 0,
          status: "published",
          lessons: {
            create: LESSONS.map((l, index) => ({
              slug: l.slug,
              title: l.title,
              order: index,
              status: "published" as const,
              difficulty: "intro" as const,
              isOptional: false,
              contentMd: l.contentMd,
              readingMinutes: computeReadingMinutes(l.contentMd),
              publishedAt: now,
            })),
          },
        },
      },
    },
  });
  return course.id;
}

/** Prepends the welcome course id to every track's course_ids (idempotent). */
export async function pinWelcomeFirstInTracks(
  db: Db,
  welcomeId: string,
  dryRun = false,
): Promise<void> {
  const tracks = await db.trackDef.findMany();
  for (const track of tracks) {
    const current = Array.isArray(track.courseIds) ? (track.courseIds as string[]) : [];
    if (current[0] === welcomeId) {
      console.log(`  ${track.key}: уже первый — пропуск.`);
      continue;
    }
    const next = [welcomeId, ...current.filter((id) => id !== welcomeId)];
    console.log(
      `  ${track.key}: ставлю welcome первым (${current.length} → ${next.length} курсов)` +
        `${dryRun ? " [dry-run]" : ""}.`,
    );
    if (!dryRun) {
      await db.trackDef.update({
        where: { key: track.key as Track },
        data: { courseIds: next },
      });
    }
  }
}
